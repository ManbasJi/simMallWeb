<script>
import { setLogin } from '@/api/login'
import { Message } from 'element-ui'
import { isvalidUsername } from '@/utils/validate'
import { setToken, setUploadToken } from '@/utils/auth'
import { setShopId, setID, setSoftType } from '@/utils/storage'
import { authTable } from '@/utils/permission'
export default {
  name: 'login',
  data() {
    const validateUsername = (rule, value, callback) => {
      if (!isvalidUsername(value)) {
        callback(new Error('请输入正确的用户名'))
      } else {
        callback()
      }
    }
    const validatePassword = (rule, value, callback) => {
      if (value.length < 6) {
        callback(new Error('密码长度不能少于6位'))
      } else {
        callback()
      }
    }
    return {
      loginForm: {
        username: '',
        password: ''
      },
      loginRules: {
        username: [{ required: true, trigger: 'blur', validator: validateUsername }],
        password: [{ required: true, trigger: 'blur', validator: validatePassword }]
      },
      passwordType: 'password',
      loading: false
    }
  },
  methods: {
    showPwd() {
      if (this.passwordType === 'password') {
        this.passwordType = ''
      } else {
        this.passwordType = 'password'
      }
    },
    handleLogin() {
      this.$refs.loginForm.validate(valid => {
        if (valid) {
          this.loading = true
          setLogin(this.loginForm.username, this.loginForm.password).then((res) => {
            this.loading = false
            if (res.code === 100) {
              Message({
                message: '密码错误次数太多,该账号已被锁定',
                type: 'error',
                duration: 3 * 1000
              })
              return
            }
            if (res.code === 404) {
              Message({
                message: '账号或密码错误',
                type: 'error',
                duration: 3 * 1000
              })
              return
            }
            if (res.code === 410) {
              Message({
                message: '该账号已被锁定',
                type: 'error',
                duration: 3 * 1000
              })
              return
            }
            console.log('token===',res.token)
            setID(res.fId)
            setToken(res.token)
            setUploadToken(res.uptoken)
            setShopId(res.fShopId)
            console.log('shopId:',res.fShopId)
            console.log('token:',res.token)
            setSoftType(res.fSoftType)
            if (authTable()) {
              Message({
                message: '登录成功',
                type: 'success',
                duration: 3 * 1000,
                onClose: () => {
                  this.$router.push({ path: '/member' })
                }
              })
            }
          }).catch((err) => {
            console.log(err)
            this.loading = false
          })
        } else {
          console.log('error submit!!')
          return false
        }
      })
    },
    afterQRScan() {
      // const hash = window.location.hash.slice(1)
      // const hashObj = getQueryObject(hash)
      // const originUrl = window.location.origin
      // history.replaceState({}, '', originUrl)
      // const codeMap = {
      //   wechat: 'code',
      //   tencent: 'code'
      // }
      // const codeName = hashObj[codeMap[this.auth_type]]
      // if (!codeName) {
      //   alert('第三方登录失败')
      // } else {
      //   this.$store.dispatch('LoginByThirdparty', codeName).then(() => {
      //     this.$router.push({ path: '/' })
      //   })
      // }
    }
  },
  created() {
    // window.addEventListener('hashchange', this.afterQRScan)
  },
  destroyed() {
    // window.removeEventListener('hashchange', this.afterQRScan)
  }
}
</script>

